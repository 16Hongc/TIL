package lecture_03;

public class Character_Ex {

	public static void main(String[] args) {
		
		char ch1 = 'A' ;                  // A : 65
		
		System.out.println(ch1) ;         // A
		System.out.println((int)ch1) ;    // 65   int형으로 출력
		
		//////////////////////////////////////////////////////
		
		ch1 = 66 ;
		
		System.out.println(ch1) ;         // B
		
		//////////////////////////////////////////////////////
		
		int ch2 = 67 ;
		
		System.out.println(ch2) ;         // 67
		System.out.println((char)ch2) ;   // C   char형으로 출력

	}

}
